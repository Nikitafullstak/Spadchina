const originalText = new WeakMap();
const originalAttributes = new WeakMap();
let observer;
let currentLanguage = 'ru';
let translating = false;

function hasCyrillic(text) {
  return /[А-Яа-яЁё]/.test(text);
}

function preserveCase(source, translated) {
  if (source.toUpperCase() === source) return translated.toUpperCase();
  if (source[0]?.toUpperCase() === source[0]) {
    return translated[0].toUpperCase() + translated.slice(1);
  }
  return translated;
}

export function translateTextToBelarusian(value) {
  if (!value || !hasCyrillic(value)) return value;

  const exact = exactTranslations[value.trim()];
  if (exact && value.trim() === value) return exact;

  let result = value;
  for (const [from, to] of phraseTranslations) {
    result = result.replaceAll(from, to);
  }

  for (const [from, to] of wordTranslations) {
    const pattern = new RegExp(`\\b${from}\\b`, 'giu');
    result = result.replace(pattern, (match) => preserveCase(match, to));
  }

  return result;
}

function shouldSkipElement(element) {
  return (
    element.closest('[data-no-translate]') ||
    ['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE'].includes(element.tagName) ||
    element.isContentEditable
  );
}

function translateNode(node) {
  if (node.nodeType === Node.TEXT_NODE) {
    const parent = node.parentElement;
    if (!parent || shouldSkipElement(parent)) return;

    if (!originalText.has(node)) {
      originalText.set(node, node.nodeValue);
    }

    const source = originalText.get(node);
    node.nodeValue = currentLanguage === 'be' ? translateTextToBelarusian(source) : source;
    return;
  }

  if (node.nodeType !== Node.ELEMENT_NODE || shouldSkipElement(node)) return;

  const attrs = ['placeholder', 'aria-label', 'title', 'alt'];
  for (const attr of attrs) {
    if (!node.hasAttribute(attr)) continue;
    let saved = originalAttributes.get(node);
    if (!saved) {
      saved = {};
      originalAttributes.set(node, saved);
    }
    if (!saved[attr]) saved[attr] = node.getAttribute(attr);
    node.setAttribute(
      attr,
      currentLanguage === 'be' ? translateTextToBelarusian(saved[attr]) : saved[attr],
    );
  }

  for (const child of node.childNodes) {
    translateNode(child);
  }
}

export function applySiteLanguage(language) {
  currentLanguage = language === 'be' ? 'be' : 'ru';
  document.documentElement.lang = currentLanguage === 'be' ? 'be' : 'ru';

  if (translating) return;
  translating = true;
  translateNode(document.getElementById('root'));
  translating = false;
}

export function initSiteTranslator() {
  const savedLanguage = localStorage.getItem('cultcode_language') === 'be' ? 'be' : 'ru';
  applySiteLanguage(savedLanguage);

  window.addEventListener('cultcode-language-change', (event) => {
    applySiteLanguage(event.detail?.language);
  });

  observer?.disconnect();
  observer = new MutationObserver(() => {
    if (translating) return;
    window.requestAnimationFrame(() => applySiteLanguage(currentLanguage));
  });
  observer.observe(document.getElementById('root'), {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: ['placeholder', 'aria-label', 'title', 'alt'],
  });
}
