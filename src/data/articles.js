const shuffle = (array) => [...array].sort(() => Math.random() - 0.5);

const getDistractors = (correct, pool) => {
  const filtered = pool.filter((item) => item !== correct);
  return shuffle(filtered).slice(0, 3);
};

const makeOptions = (correct, distractors) => {
  const options = shuffle([correct, ...distractors]);
  return {
    options,
    correct: options.indexOf(correct),
  };
};

const makeSingleQuestion = (id, question, correct, distractors) => {
  const result = makeOptions(correct, distractors);
  return {
    id,
    type: 'single',
    question,
    options: result.options,
    correct: result.correct,
  };
};

