func ensureTeamBattleQuestionMinimums() {
	categories := []struct {
		Category string
		Label    string
		BaseID   int
	}{
		{"history", "История", 810000},
		{"culture", "Культура", 820000},
		{"nature", "Природа", 830000},
		{"traditions", "Традиции", 840000},
	}

	for _, item := range categories {
		current := countQuestionsForCategory(item.Category)
		if current >= 20 {
			continue
		}

		missing := 20 - current
		questions := makeTeamBattleSupplementQuestions(item.Category, item.Label, item.BaseID+current, missing)
		questionsJSON, _ := json.Marshal(questions)
		content := fmt.Sprintf(`[{"type":"lead","text":"Дополнительный набор вопросов для командных батлов по теме «%s»."}]`, item.Label)

		_, err := db.Exec(
			"INSERT INTO articles (title, category, category_label, difficulty, difficulty_label, read_time, image, content, questions) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)",
			fmt.Sprintf("Командные вопросы: %s", item.Label),
			item.Category,
			item.Label,
			"medium",
			"Средний",
			3,
			"",
			content,
			string(questionsJSON),
		)
		if err != nil {
			log.Printf("team battle question seed error: %v", err)
		}
	}
}

func countQuestionsForCategory(category string) int {
	rows, err := db.Query("SELECT questions FROM articles WHERE category = ?", category)
	if err != nil {
		return 0
	}
	defer rows.Close()

	total := 0
	for rows.Next() {
		var raw string
		if err := rows.Scan(&raw); err != nil {
			continue
		}
		var questions []DuelQuestion
		if err := json.Unmarshal([]byte(raw), &questions); err == nil {
			total += len(questions)
		}
	}
	return total
}

